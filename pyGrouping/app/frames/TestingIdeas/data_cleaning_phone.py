def remove_duplicate_phone_numbers(phone_numbers):
    unique_numbers = set(phone_numbers)
    unique_list = list(unique_numbers)
    phone_numbers[:] = unique_list  # Update the original list
    return phone_numbers


def remove_acd_line(phone_numbers):
    acd_line = []
    for num in phone_numbers[:]:  # Use a copy of the list to avoid modifying it while iterating
        if num.startswith("209"):
            acd_line.append(num)
            phone_numbers.remove(num)
    return phone_numbers, acd_line


